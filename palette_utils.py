def _validate_palette_index(index):
    if isinstance(index, bool) or not isinstance(index, int):
        raise TypeError(f'palette index must be an integer, got {index!r}')

    if not 1 <= index <= 255:
        raise ValueError(f'palette index must be between 1 and 255, got {index!r}')


def _normalize_palette_color(index, color):
    if isinstance(color, Color):
        rgba = tuple(color)
    elif isinstance(color, (tuple, list)):
        rgba = tuple(color)
    else:
        raise TypeError(f'palette color for index {index} must be a Color or a tuple/list, got {color!r}')

    if len(rgba) == 3:
        rgba = (*rgba, 255)
    elif len(rgba) != 4:
        raise ValueError(f'palette color for index {index} must have 3 or 4 channels, got {rgba!r}')

    normalized_channels = []
    for channel in rgba:
        if isinstance(channel, bool) or not isinstance(channel, int):
            raise TypeError(f'palette channel for index {index} must be an integer, got {channel!r}')
        if not 0 <= channel <= 255:
            raise ValueError(f'palette channel for index {index} must be between 0 and 255, got {channel!r}')
        normalized_channels.append(channel)

    return Color(*normalized_channels)


def validate_palette_overrides(overrides):
    if overrides is None:
        return

    if not isinstance(overrides, dict):
        raise TypeError('BLUEPRINT_PALETTE_OVERRIDES must be a dictionary of palette index to RGB/RGBA tuples.')

    for index, color in overrides.items():
        _validate_palette_index(index)
        _normalize_palette_color(index, color)


def build_palette(overrides=None):
    validate_palette_overrides(overrides)

    palette = [Color(*color) for color in MAGICA_DEFAULT_PALETTE]

    if overrides:
        for index, color in overrides.items():
            palette[index] = _normalize_palette_color(index, color)

    return palette